package mainmethod;

public class Forloop // 10 to 1

{
	public static void main(String[] args) {
		
		int i;
		int j;
		
		System.out.println("printing 10 to 1..");
		for(i=10;i>=1;i--)
		System.out.println(i);
		
		System.out.println("printing 1 to 10..");
		
		for(j=1;j<=10;j++)
		System.out.println(j);

		
	}
	
	

}
                  
